<?php
function sum($a, $b)
{
    return $a + $b;
}

function expo($nbr)
{
    return exp($nbr);
}

function diff($a, $b)
{
    return $a - $b;
}

function prod($a, $b)
{
    return $a * $b;
}

function mod($a, $b)
{
    return $a % $b;
}

function div($a, $b)
{
    return $a / $b;
}

function carre($nbr)
{
    return pow($nbr, 2);
}
